fn show_main_window(app: &AppHandle) {
    if let Some(window) = app.get_webview_window("main") {
        let _ = window.show();
        let _ = window.unminimize();
        let _ = window.set_focus();
    }
}

fn show_near_tray(app: &AppHandle, click_x: f64, click_y: f64) {
    if let Some(window) = app.get_webview_window("main") {
        let scale = window.scale_factor().unwrap_or(1.0);
        let logical_x = click_x / scale;
        let logical_y = click_y / scale;
        let win_width = 206.0_f64;
        let x = (logical_x - win_width / 2.0).max(4.0);
        let y = logical_y + 4.0;
        let _ = window.set_position(LogicalPosition::new(x, y));
        let _ = window.show();
        let _ = window.set_focus();
    }
}

fn hide_main_window(app: &AppHandle) {
    if let Some(window) = app.get_webview_window("main") {
        let _ = window.hide();
    }
}

fn build_tray(app: &AppHandle) -> tauri::Result<()> {
    let open = MenuItem::with_id(app, "open", "Open Watchdog", true, None::<&str>)?;
    let quit = MenuItem::with_id(app, "quit", "Quit", true, None::<&str>)?;
    let menu = Menu::with_items(app, &[&open, &quit])?;

    let tray_icon = menu_bar_icon();

    let tray = TrayIconBuilder::with_id("watchdog")
        .icon(tray_icon)
        .icon_as_template(true)
        .tooltip("Claude Credit Watchdog")
        .menu(&menu)
        .show_menu_on_left_click(false)
        .on_menu_event(|app, event| match event.id().as_ref() {
            "open" => show_main_window(app),
            "quit" => app.exit(0),
            _ => {}
        })
        .on_tray_icon_event(|tray, event| {
            if let TrayIconEvent::Click {
                button: MouseButton::Left,
                button_state: MouseButtonState::Up,
                position,
                ..
            } = event
            {
                let app = tray.app_handle();
                if let Some(window) = app.get_webview_window("main") {
                    if window.is_visible().unwrap_or(false) {
                        hide_main_window(&app);
                    } else {
                        show_near_tray(&app, position.x, position.y);
                    }
                }
            }
        });

    tray.build(app)?;

    Ok(())
}

fn main() {
    tauri::Builder::default()
        .setup(|app| {
            let _ = app.set_activation_policy(ActivationPolicy::Accessory);
            let _ = app.set_dock_visibility(false);
            build_tray(app.handle())?;
            Ok(())
        })
        .invoke_handler(tauri::generate_handler![
            get_sessions,
            get_overview,
            resume_session,
            resume_due_sessions,
            dismiss_alert,
            toggle_watcher,
            get_settings,
            save_settings,
            open_log_dir
        ])
        .run(tauri::generate_context!())
        .expect("error while running Claude Credit Watchdog");
}
